//! Lockfile format — the *resolved exact pins* for a recipe. Populated from
//! upstream by `boot2deb update`; `build` reads only the lock and never resolves
//! "latest".

use crate::model::ExtraDeb;
use serde::{Deserialize, Serialize};

/// Deserialize a lock commit id, enforcing the full-40-hex-sha shape at the parse
/// boundary (SUB-3). `build` feeds these values straight into option-sensitive `git`
/// argv positions — the detached checkout and `fetch <commit>` — where a leading `-`
/// or other malformed value could be reinterpreted; pinning the shape at the lock
/// boundary means a hand-edited or corrupt lock cannot smuggle one that far. The
/// value is lowercased to git's canonical output form so a later `HEAD == commit`
/// byte comparison holds regardless of the case a hand-editor typed.
fn de_commit<'de, D>(d: D) -> Result<String, D::Error>
where
    D: serde::Deserializer<'de>,
{
    let s = String::deserialize(d)?;
    if crate::sources::is_full_sha(&s) {
        Ok(s.to_ascii_lowercase())
    } else {
        Err(serde::de::Error::custom(format!(
            "commit '{s}' is not a full 40-character hex sha"
        )))
    }
}

/// Shape-check one `"<filename>@sha256:<hex64>"` blob pin (TRUST-7): a bare
/// filename (non-empty, no separators, not option-like) and a 64-char
/// lowercase-hex digest — the exact form `update`'s pin generator emits.
/// Returns the failure reason for a deserializer to wrap.
fn check_blob_pin(pin: &str) -> Result<(), String> {
    let Some((file, hex)) = pin.rsplit_once("@sha256:") else {
        return Err("has no '@sha256:' separator".into());
    };
    if file.is_empty() || file.contains('/') || file.contains('\\') || file.starts_with('-') {
        return Err("has an invalid filename component".into());
    }
    if !crate::sources::is_sha256_hex(hex) {
        return Err("digest is not 64 lowercase hex characters".into());
    }
    Ok(())
}

/// Deserialize a blob pin, enforcing the `"<filename>@sha256:<hex64>"` shape at
/// the parse boundary (TRUST-7, the [`de_commit`] discipline): the filename is
/// later joined into blob-dir paths and the digest compared as bytes, so a
/// hand-edited or corrupt lock must fail here, attributably, not downstream.
fn de_blob_pin<'de, D>(d: D) -> Result<String, D::Error>
where
    D: serde::Deserializer<'de>,
{
    let s = String::deserialize(d)?;
    check_blob_pin(&s).map_err(|why| serde::de::Error::custom(format!("blob pin '{s}' {why}")))?;
    Ok(s)
}

/// [`de_blob_pin`] for the optional BL32 pin.
fn de_opt_blob_pin<'de, D>(d: D) -> Result<Option<String>, D::Error>
where
    D: serde::Deserializer<'de>,
{
    match Option::<String>::deserialize(d)? {
        Some(s) => {
            check_blob_pin(&s)
                .map_err(|why| serde::de::Error::custom(format!("blob pin '{s}' {why}")))?;
            Ok(Some(s))
        }
        None => Ok(None),
    }
}

/// Deserialize the manifest filename as a *bare* name (TRUST-7): it is joined
/// into `recipes/<manifest>` paths, so separators or dot-dirs must fail at the
/// parse boundary rather than address a file outside `recipes/`.
fn de_bare_filename<'de, D>(d: D) -> Result<String, D::Error>
where
    D: serde::Deserializer<'de>,
{
    let s = String::deserialize(d)?;
    let bad = s.is_empty()
        || s.contains('/')
        || s.contains('\\')
        || s == "."
        || s == ".."
        || s.starts_with('-');
    if bad {
        return Err(serde::de::Error::custom(format!(
            "manifest '{s}' is not a bare filename"
        )));
    }
    Ok(s)
}

/// Deserialize the optional manifest digest as 64 lowercase hex (TRUST-7).
fn de_opt_sha256<'de, D>(d: D) -> Result<Option<String>, D::Error>
where
    D: serde::Deserializer<'de>,
{
    match Option::<String>::deserialize(d)? {
        Some(s) if crate::sources::is_sha256_hex(&s) => Ok(Some(s)),
        Some(s) => Err(serde::de::Error::custom(format!(
            "sha256 '{s}' is not 64 lowercase hex characters"
        ))),
        None => Ok(None),
    }
}

/// Header prepended to a serialized lock, marking it generated.
const LOCK_BANNER: &str = "\
# Generated by `boot2deb update`; do not hand-edit.
# Exact resolved pins for the recipe. `boot2deb build` reads only this;
# re-run `boot2deb update` to re-resolve within the recipe's constraints.
";

/// The resolved exact pins for a recipe. `boot2deb build` reads only this;
/// `boot2deb update` is the sole writer, re-resolving upstream within the
/// recipe's constraints. Keeping old locks is what keeps old builds reproducible.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(deny_unknown_fields)]
pub struct Lock {
    /// Exact kernel pin. Present iff the build **compiles** a kernel. A
    /// distro-package kernel is installed from the mirror like any other package, so
    /// its exact version and hash live in the solved package manifest
    /// ([`RootfsPin::manifest`]) and there is no commit to pin here; the committed
    /// lock then omits the `[kernel]` table entirely, rather than recording a source
    /// dependency the build does not have.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub kernel: Option<KernelPin>,
    /// Exact patch-series pin. Present iff the resolved kernel names a patch
    /// profile. A kernel that applies no series never reads the `patches` repo, so
    /// pinning a commit would record provenance for a dependency the build does not
    /// have; the committed lock then omits the `[patches]` table entirely.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub patches: Option<PatchesPin>,
    /// Exact u-boot pin. Present iff the build's boot method **compiles u-boot**
    /// (`rockchip-rkbin`). A depthcharge board's firmware is its own — coreboot in an
    /// SPI chip — so nothing bootloader-shaped is fetched or built, and the committed
    /// lock omits the `[uboot]` table.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub uboot: Option<UbootPin>,
    /// Exact media-accel userspace source pins (MPP/RGA/Mali). Present iff the
    /// recipe builds the HW transcode stack (a `requires_media_accel` feature is
    /// selected); omitted from the committed lock for a base build, which resolves
    /// and builds no userspace/ffmpeg nodes.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub userspace: Option<UserspacePins>,
    /// Exact ffmpeg source pins (V4L2 base + Rockchip rkmpp/rkrga). Present in
    /// lockstep with [`userspace`](Self::userspace) — the media-accel stack pins
    /// as a unit.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub ffmpeg: Option<FfmpegPins>,
    /// Rootfs suite + content-pinned package manifest.
    pub rootfs: RootfsPin,
    /// Verified rkbin blob hashes. Present iff the build's boot method consumes rkbin
    /// blobs (`rockchip-rkbin`, whose u-boot build takes the ATF and DDR TPL as make
    /// inputs). Omitted from the committed lock otherwise.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub blobs: Option<BlobsPin>,
    /// Pre-built `.deb`s pulled from outside the Debian mirror,
    /// content-pinned by sha256 — the resolved [`ExtraDeb`] set recorded verbatim
    /// (the sha256 is already exact, so there is nothing to resolve). `update`
    /// fetches + verifies + stores each; `build` materializes from the store into
    /// the local apt repo. Empty (and omitted from the committed file) when no layer
    /// or feature adds one.
    #[serde(default, skip_serializing_if = "Vec::is_empty")]
    pub extra_debs: Vec<ExtraDeb>,
    /// `snapshot.debian.org` availability backstop; captured but dormant.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub snapshot: Option<SnapshotPin>,
}

impl Lock {
    /// Serialize to the canonical committed form: the generated-file banner
    /// followed by the TOML body. This is the exact text `boot2deb update`
    /// writes to `recipes/<name>.lock`.
    pub fn to_toml_string(&self) -> Result<String, crate::ConfigError> {
        let body = toml::to_string(self).map_err(|source| crate::ConfigError::Serialize {
            what: "lock",
            source,
        })?;
        Ok(format!("{LOCK_BANNER}{body}"))
    }
}

/// Pinned kernel: which definition, from which repo, at which ref, resolved to
/// which commit.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(deny_unknown_fields)]
pub struct KernelPin {
    /// Kernel definition id.
    pub id: String,
    /// Clone URL the commit was pinned from (the definition's resolved source).
    /// A commit id is meaningful only within its repo, so every commit pin
    /// records its source; the drift gate compares it against a fresh resolve,
    /// catching a config edit that would reinterpret the pin in a different
    /// repo (COR-23).
    pub source: String,
    /// The tag/branch that was resolved (TOML key `ref`).
    #[serde(rename = "ref")]
    pub reference: String,
    /// The exact commit the ref pointed at. Deserialization enforces the full-hex-sha
    /// shape (SUB-3).
    #[serde(deserialize_with = "de_commit")]
    pub commit: String,
}

/// Pinned patch series: the profile and the exact `patches`-repo commit.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(deny_unknown_fields)]
pub struct PatchesPin {
    /// Patch profile name.
    pub profile: String,
    /// `patches` repo commit. Deserialization enforces the full-hex-sha
    /// shape (SUB-3).
    #[serde(deserialize_with = "de_commit")]
    pub commit: String,
}

/// Pinned u-boot: source repo, ref, and exact commit.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(deny_unknown_fields)]
pub struct UbootPin {
    /// Clone URL the commit was pinned from (the resolved boot method's
    /// `uboot_source`). The drift gate compares it against a fresh resolve, so
    /// a boot-method flip to a different repo cannot silently reinterpret the
    /// commit there (COR-23).
    pub source: String,
    /// The tag that was resolved (TOML key `ref`).
    #[serde(rename = "ref")]
    pub reference: String,
    /// The exact commit the ref pointed at. Deserialization enforces the full-hex-sha
    /// shape (SUB-3).
    #[serde(deserialize_with = "de_commit")]
    pub commit: String,
}

/// A resolved git pin: the source repo, the ref that was requested, and the
/// exact commit it resolved to. Shared by the userspace and ffmpeg source pins.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(deny_unknown_fields)]
pub struct GitPin {
    /// Clone URL the commit was pinned from (the resolved SoC-layer source).
    /// Compared against a fresh resolve by the drift gate (COR-23).
    pub source: String,
    /// The branch/tag/commit that was requested (TOML key `ref`).
    #[serde(rename = "ref")]
    pub reference: String,
    /// The exact commit the ref pointed at. Deserialization enforces the full-hex-sha
    /// shape (SUB-3).
    #[serde(deserialize_with = "de_commit")]
    pub commit: String,
}

/// Pinned media-accel userspace sources — the MPP/RGA/Mali forks the userspace
/// build node compiles into `.deb`s.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(deny_unknown_fields)]
pub struct UserspacePins {
    /// Rockchip MPP pin (`librockchip-mpp1`).
    pub mpp: GitPin,
    /// librga pin (`librga2`).
    pub librga: GitPin,
    /// libmali pin (built only when requested).
    pub libmali: GitPin,
}

/// Pinned ffmpeg sources — the V4L2 base plus the Rockchip provenance tree the
/// graft patch series was derived from. `build` fetches `base` and applies
/// the profile's ffmpeg `git am` series; the graft is not re-derived from
/// `rockchip`, which is recorded purely as provenance.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(deny_unknown_fields)]
pub struct FfmpegPins {
    /// Base tree pin (V4L2-request decode).
    pub base: GitPin,
    /// Rockchip rkmpp/rkrga provenance-tree pin the graft series was derived from.
    pub rockchip: GitPin,
}

/// Pinned rootfs: the suite plus a pointer to the solved package manifest that
/// content-pins every package (name → version → sha256) — the reproducibility
/// guarantee.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(deny_unknown_fields)]
pub struct RootfsPin {
    /// Debian suite.
    pub suite: String,
    /// Filename of the content-pinned solved package manifest, committed beside
    /// the lock (`recipes/<manifest>`). Deserialization enforces a bare filename
    /// (TRUST-7).
    #[serde(deserialize_with = "de_bare_filename")]
    pub manifest: String,
    /// sha256 of that committed manifest file, pinning the manifest itself so a
    /// committed-manifest that drifts from the lock is caught. `None` until
    /// the manifest is committed beside the lock — the manifest-as-input wiring
    /// sets it; a bare `update` before any build leaves it unset. Deserialization
    /// enforces the 64-lowercase-hex shape (TRUST-7).
    #[serde(
        default,
        skip_serializing_if = "Option::is_none",
        deserialize_with = "de_opt_sha256"
    )]
    pub manifest_sha256: Option<String>,
}

/// Pinned rkbin blobs, each `"<filename>@sha256:<hex>"`, verified before the
/// u-boot build consumes them.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(deny_unknown_fields)]
pub struct BlobsPin {
    /// ATF/BL31 blob pin. Deserialization enforces the
    /// `"<filename>@sha256:<hex64>"` shape (TRUST-7).
    #[serde(deserialize_with = "de_blob_pin")]
    pub atf: String,
    /// DDR TPL blob pin. Shape-enforced like [`atf`](Self::atf) (TRUST-7).
    #[serde(deserialize_with = "de_blob_pin")]
    pub tpl: String,
    /// OP-TEE BL32 blob pin, present only when the build has one. Absent on
    /// BL31-only boots (RK3588/RK1), so those locks serialize without the field.
    /// Shape-enforced like [`atf`](Self::atf) when present (TRUST-7).
    #[serde(
        default,
        skip_serializing_if = "Option::is_none",
        deserialize_with = "de_opt_blob_pin"
    )]
    pub bl32: Option<String>,
}

/// How a captured snapshot participates in a build.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "lowercase")]
pub enum SnapshotMode {
    /// Provenance only; builds use the live mirror.
    Off,
    /// Live mirror first, snapshot fills anything that 404s.
    Fallback,
    /// Build exclusively against the snapshot.
    Pin,
}

impl SnapshotMode {
    /// The mode's lowercase name — matches the serialized TOML form and the
    /// `--snapshot` CLI value, so error messages and the flag agree.
    pub fn as_str(self) -> &'static str {
        match self {
            SnapshotMode::Off => "off",
            SnapshotMode::Fallback => "fallback",
            SnapshotMode::Pin => "pin",
        }
    }
}

/// A captured `snapshot.debian.org` timestamp and its activation mode.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(deny_unknown_fields)]
pub struct SnapshotPin {
    /// Snapshot timestamp (e.g. `20260628T083000Z`).
    pub timestamp: String,
    /// Whether/how the snapshot is used.
    pub mode: SnapshotMode,
}

