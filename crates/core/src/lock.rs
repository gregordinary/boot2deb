    #[test]
    fn a_distro_kernel_depthcharge_lock_pins_only_the_rootfs() {
        // The C201 shape: Debian's own kernel package, a boot method that compiles no
        // u-boot, and no rkbin blobs. Nothing is fetched from git and nothing is
        // compiled, so the only thing there *is* to pin is the package set — and the
        // committed lock says exactly that, rather than carrying three tables of
        // provenance for dependencies the build does not have.
        let lock = Lock {
            kernel: None,
            patches: None,
            uboot: None,
            userspace: None,
            ffmpeg: None,
            rootfs: RootfsPin {
                suite: "forky".into(),
                manifest: "asus-c201-forky.pkgs.lock".into(),
                manifest_sha256: None,
            },
            blobs: None,
            extra_debs: vec![],
            snapshot: None,
        };
        let text = lock.to_toml_string().unwrap();
        for absent in ["[kernel]", "[patches]", "[uboot]", "[blobs]", "[userspace", "[ffmpeg"] {
            assert!(
                !text.contains(absent),
                "a distro-kernel depthcharge lock must omit {absent}:\n{text}"
            );
        }
        assert!(text.contains("[rootfs]"));
        // And it round-trips: every omitted table reads back as an absence, not a
        // default-constructed pin.
        assert_eq!(toml::from_str::<Lock>(&text).unwrap(), lock);
    }

    #[test]
    fn a_no_patch_kernels_lock_omits_the_patches_table() {
        // A kernel that applies no series pins no `patches` commit: the committed
        // lock has no `[patches]` table at all, and round-trips back to `None` rather
        // than to a placeholder pin.
        let mut lock = base_lock();
        lock.patches = None;
        let text = lock.to_toml_string().unwrap();
        assert!(
            !text.contains("[patches]"),
            "no-patch lock must omit the table entirely:\n{text}"
        );
        assert_eq!(toml::from_str::<Lock>(&text).unwrap(), lock);
        // A lock that does pin a series still writes the table.
        let with = base_lock().to_toml_string().unwrap();
        assert!(with.contains("[patches]"));
        assert!(with.contains("profile = \"rk3588-accel\""));
    }

    #[test]
    fn deserialize_rejects_a_non_sha_commit() {
        // A lock whose kernel commit is not a full 40-hex sha is refused at parse
        // time (SUB-3), so a malformed/hand-edited value never reaches the git argv.
        let text = base_lock().to_toml_string().unwrap();
        let tampered = text.replacen(
            &format!("commit = \"{}\"", "a".repeat(40)),
            "commit = \"-o=evil\"",
            1,
        );
        assert!(
            toml::from_str::<Lock>(&tampered).is_err(),
            "a non-sha commit must be rejected at deserialize"
        );
        // An uppercase sha is accepted but canonicalized to git's lowercase form.
        let upper = text.replacen(
            &format!("commit = \"{}\"", "a".repeat(40)),
            &format!("commit = \"{}\"", "A".repeat(40)),
            1,
        );
        let back: Lock = toml::from_str(&upper).unwrap();
        assert_eq!(back.kernel.unwrap().commit, "a".repeat(40));
    }

    #[test]
    fn deserialize_rejects_malformed_blob_and_manifest_shapes() {
        // Every content-pin field is shape-checked at the parse boundary
        // (TRUST-7), so a hand-edited lock fails here, naming the field, rather
        // than misbehaving downstream where the value is joined into paths or
        // compared against hashes.
        let good = base_lock().to_toml_string().unwrap();
        let atf = format!("atf@sha256:{}", "0".repeat(64));
        assert!(good.contains(&atf), "fixture must carry the expected pin");
        for (from, to, why) in [
            // No separator at all.
            (atf.clone(), "atf-no-separator".to_string(), "separator"),
            // Digest too short / wrong case.
            (atf.clone(), format!("atf@sha256:{}", "0".repeat(63)), "64 lowercase hex"),
            (atf.clone(), format!("atf@sha256:{}", "A".repeat(64)), "64 lowercase hex"),
            // Filename with a path separator.
            (atf.clone(), format!("../atf@sha256:{}", "0".repeat(64)), "filename"),
            // Manifest must be a bare filename.
            (
                "manifest = \"rec.pkgs.lock\"".to_string(),
                "manifest = \"../rec.pkgs.lock\"".to_string(),
                "bare filename",
            ),
        ] {
            let bad = good.replacen(&from, &to, 1);
            assert_ne!(good, bad, "case failed to modify the fixture: {to}");
            let err = toml::from_str::<Lock>(&bad).unwrap_err().to_string();
            assert!(err.contains(why), "expected '{why}' in: {err}");
        }
        // manifest_sha256, when present, must be 64 lowercase hex too.
        let with_digest = good.replacen(
            "manifest = \"rec.pkgs.lock\"",
            "manifest = \"rec.pkgs.lock\"\nmanifest_sha256 = \"DEADBEEF\"",
            1,
        );
        let err = toml::from_str::<Lock>(&with_digest).unwrap_err().to_string();
        assert!(err.contains("64 lowercase hex"), "{err}");
    }

    #[test]
    fn lock_without_snapshot_omits_the_table() {
        let lock = base_lock();
        let text = lock.to_toml_string().unwrap();
        // The reserved/optional fields collapse out of the committed form when
        // unset, so the shipped lock stays minimal until a mechanism populates them.
        assert!(
            !text.contains("[snapshot]") && !text.contains("snapshot ="),
            "committed lock must not emit a snapshot table when None:\n{text}"
        );
        assert!(
            !text.contains("extra_debs"),
            "empty extra_debs must be omitted:\n{text}"
        );
        assert!(
            !text.contains("manifest_sha256"),
            "unset manifest_sha256 must be omitted:\n{text}"
        );
        let back: Lock = toml::from_str(&text).unwrap();
        assert_eq!(back.snapshot, None);
        assert!(back.extra_debs.is_empty());
        assert_eq!(back.rootfs.manifest_sha256, None);
        assert_eq!(lock, back);
    }

    #[test]
    fn base_build_lock_omits_the_media_accel_tables() {
        // A recipe with no `requires_media_accel` feature builds no userspace/ffmpeg
        // nodes, so `update` writes no pins for them. The committed lock must omit
        // both tables entirely (not empty tables) and round-trip back to `None`.
        let mut lock = base_lock();
        lock.userspace = None;
        lock.ffmpeg = None;
        let text = lock.to_toml_string().unwrap();
        assert!(
            !text.contains("[userspace") && !text.contains("[ffmpeg"),
            "base-build lock must not emit media-accel tables:\n{text}"
        );
        let back: Lock = toml::from_str(&text).unwrap();
        assert_eq!(back.userspace, None);
        assert_eq!(back.ffmpeg, None);
        assert_eq!(lock, back);
    }

    #[test]
    fn bl32_blob_pin_round_trips_and_is_omitted_when_absent() {
        // BL31-only build (RK3588/RK1): no bl32 key in the committed form, parses
        // back to None, and an older lock written without the key still loads.
        let lock = base_lock();
        assert_eq!(lock.blobs.as_ref().unwrap().bl32, None);
        let text = lock.to_toml_string().unwrap();
        assert!(!text.contains("bl32"), "absent bl32 must be omitted:\n{text}");
        assert_eq!(
            toml::from_str::<Lock>(&text).unwrap().blobs.unwrap().bl32,
            None
        );

        // OP-TEE build (RK3576): the pin is present and survives the round-trip.
        let mut with = base_lock();
        with.blobs.as_mut().unwrap().bl32 = Some("rk3576_bl32_v1.08.bin@sha256:aaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaa".into());
        let text = with.to_toml_string().unwrap();
        assert!(text.contains("bl32 = \"rk3576_bl32_v1.08.bin@sha256:aaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaa\""));
        assert_eq!(toml::from_str::<Lock>(&text).unwrap(), with);
    }
